var citysVisited = ["Boston", "Atlanta", "New York", "Chicago" ]
var jerseyNumbers = [15, 20, 22, 10]
var hasSurfed = [true, true, false, true]

// The pop method will remove the last element from the array and returns that element. 
// In this case it will return Chicago. 
console.log(citysVisited.pop())

// The push method will add one or more elements to the end of an array & return length of new array.
console.log(jerseyNumbers.push())

// The shift method removes the first element in the array and returns that the removed element. 
console.log(hasSurfed.shift())


